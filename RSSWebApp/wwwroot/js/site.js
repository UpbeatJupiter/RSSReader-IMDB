function findWord() {
    var table = document.getElementById("haberTablosu");
    var input = document.getElementById("find");

    let filter = input.value.toUpperCase(); //get input value
    rows = table.getElementsByTagName("tr"); //get all the tr elements
    let flag = false;

    for (let row of rows) {         //loop through all the TR elements and get the underlying TD elements
        let cells = row.getElementsByTagName("td");
        for (let cell of cells) {   //perform string comparison between the input value and value of textContent inside each TD elements
            if (cell.textContent.toUpperCase().indexOf(filter) > -1) {
                flag = true;
                break;
            }
        }

        if (flag) {
            row.style.display = ""; //modify the TR element CSS to be hidden or display based on the previous string comparison
           
        } else {
            row.style.display = "none";
        }

        flag = false;
    }
}

